function Personal(fullname, gender, phone, dob, nid, doi, doe, city, district, ward, street) {
    this.fullname = fullname;
    this.gender = gender;
    this.phone = phone;
    this.dob = dob;
    this.nid = nid;
    this.doi = doi;
    this.doe = doe;
    this.city = city;
    this.district = district;
    this.ward = ward;
    this.street = street;
}